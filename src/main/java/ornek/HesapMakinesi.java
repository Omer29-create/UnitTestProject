package ornek;

public class HesapMakinesi {
    public int topla  ( int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int cikar  ( int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int metreKareHesap  ( int en, int boy) {
        return en * boy;
    }
}
